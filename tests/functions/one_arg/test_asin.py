"""Tests for asin()."""

from formula import Formula, FmtFlags


def test_asin_zero():
    assert Formula("asin(0)").get() == "0"


def test_asin_precision_256():
    expected = "0.5235987755982988730771072305465838140328615665625176368291574320513027343810348331046724708903528446636913477522137177745156407682584303719542265680214135195750473504503230868509266074371581591550636607381351626109889076880792747056311305275452003181909414"
    got = Formula("asin(1/2)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
