"""Tests for exp()."""

from formula import Formula, FmtFlags


def test_exp_zero():
    assert Formula("exp(0)").get() == "1"


def test_exp_precision_256():
    expected = "2.7182818284590452353602874713526624977572470936999595749669676277240766303535475945713821785251664274274663919320030599218174135966290435729003342952605956307381323286279434907632338298807531952510190115738341879307021540891499348841675092447614606680822648"
    got = Formula("exp(1)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
