"""Tests for tan()."""

from formula import Formula, FmtFlags


def test_tan_zero():
    assert Formula("tan(0)").get() == "0"


def test_tan_precision_256():
    expected = "1.5574077246549022305069748074583601730872507723815200383839466056988613971517272895550999652022429838046338214117481666133235546181245589376060716845489044392935860431671479080368246132747069555973416406107755352473025067968505070413523851449176214816275700"
    got = Formula("tan(1)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
