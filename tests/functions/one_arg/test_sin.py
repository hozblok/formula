"""Tests for sin()."""

from formula import Formula, FmtFlags


def test_sin_zero():
    assert Formula("sin(0)").get() == "0"


def test_sin_precision_256():
    expected = "0.8414709848078965066525023216302989996225630607983710656727517099919104043912396689486397435430526958543490379079206742932591189209918988811934103277292124094807919558267666069999077640119784087827325663474848028702986561570179624553948935729246701270864863"
    got = Formula("sin(1)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
