"""Tests for sqrt()."""

from formula import Formula, FmtFlags


def test_sqrt_four():
    assert Formula("sqrt(4)").get() == "2"


def test_sqrt_precision_256():
    expected = "1.4142135623730950488016887242096980785696718753769480731766797379907324784621070388503875343276415727350138462309122970249248360558507372126441214970999358314132226659275055927557999505011527820605714701095599716059702745345968620147285174186408891986095523"
    got = Formula("sqrt(2)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
