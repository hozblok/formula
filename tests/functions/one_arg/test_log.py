"""Tests for log() (natural logarithm)."""

from formula import Formula, FmtFlags


def test_log_one():
    assert Formula("log(1)").get() == "0"


def test_log_precision_256():
    expected = "0.6931471805599453094172321214581765680755001343602552541206800094933936219696947156058633269964186875420014810205706857336855202357581305570326707516350759619307275708283714351903070386238916734711233501153644979552391204751726815749320651555247341395258830"
    got = Formula("log(2)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
