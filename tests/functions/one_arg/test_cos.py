"""Tests for cos()."""

from formula import Formula, FmtFlags


def test_cos_zero():
    assert Formula("cos(0)").get() == "1"


def test_cos_precision_256():
    expected = "0.5403023058681397174009366074429766037323104206179222276700972553811003947744717645179518560871830893435717311600300890978606337600216634564065122654173185847179711644744794942331179245513932543359435177567028925963757361543275496417544917751151312227301006"
    got = Formula("cos(1)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
