"""Tests for acos()."""

from formula import Formula, FmtFlags


def test_acos_one():
    assert Formula("acos(1)").get() == "0"


def test_acos_precision_256():
    expected = "1.0471975511965977461542144610931676280657231331250352736583148641026054687620696662093449417807056893273826955044274355490312815365168607439084531360428270391500947009006461737018532148743163183101273214762703252219778153761585494112622610550904006363818829"
    got = Formula("acos(1/2)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
