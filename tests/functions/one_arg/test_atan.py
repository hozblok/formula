"""Tests for atan()."""

from formula import Formula, FmtFlags


def test_atan_zero():
    assert Formula("atan(0)").get() == "0"


def test_atan_precision_256():
    expected = "0.7853981633974483096156608458198757210492923498437764552437361480769541015715522496570087063355292669955370216283205766617734611523876455579313398520321202793625710256754846302763899111557372387325954911072027439164833615321189120584466957913178004772864121"
    got = Formula("atan(1)", 256).get(digits=256, format=FmtFlags.fixed)
    assert got == expected
